public class Math {

	/***
	 * Sum two integers
	 * @param a
	 * @param b
	 * @return the result as integer
	 */
	public static int sum(int a, int b) {
		return a + b;
	}
	
	/***
	 * Divide two integers
	 * @param a
	 * @param b
	 * @return the result as integer
	 */
	public static int divide(int a, int b) {
		return (int) a / b;
	}
	
}
